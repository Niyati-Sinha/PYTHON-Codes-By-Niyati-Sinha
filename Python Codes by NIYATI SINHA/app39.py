#LISTS
names=['Niyati','Nisha','Naveen','Mohani','Bhajan']
print(names)
print(names[0])#0th index value in names
print(names[-1])#last index value in names
print("______________________________________")
print(names[2:])#all the items from 2nd index in list
print("______________________________________")
print(names[2:4])#all the items from lower index in list to upper-1 index
print("______________________________________")
print(names[:])#all the items in the list
#using names[a:b] does not modify the original list it just simply returns a new list